      <div class="span8">
        <h2 class="pad-b--20">Academia De Futebol Training Bylaws</h2>
        <ul class="pad-b--20">
          <li>Guidelines, Procedures, and Bylaws</li>
          <li>January, 1, 2015</li>
        </ul>
        <h3>Purpose:</h3>
        <p class="pad-b--20">This document is for the purpose of establishing your responsibilities as a coach or board member at Academia De Futeboltraining (ADF). You will be responsible for keeping all academy policies as outlined in the attached guidelines.</p>

        <h3>Founding Principles:</h3>
        <p>The general purpose of Academia De Futeboltraining will be to provide a positive, fun-filled environment, encouraging individual player growth beyond that normally available through recreational soccer.</p>
        <p>The Academy’s governing organization is comprised of a united group of individuals committed to every player’s development. The academy will empower and allow individuals with soccer experience to make all decisions regarding player development and coaching consistent with the academy’s adopted philosophy. The academy will also empower and allow individuals with administrative experience to make all decisions regarding administration of the academy for the benefit of the players consistent with the academy’s adopted philosophy.</p>
        <p class="pad-b--20">Academia De Futeboltraining is an affiliate of the Oregon Premier League (OPL) and the United States academy Soccer Association (USCS).</p>

        <h4>Players Shall:</h4>
        <ul class="pad-b--20">
          <li>Be of high moral character.</li>
          <li>Be knowledgeable in basic soccer skills and the principles of team play.</li>
          <li>Provide opportunities for individual growth of all players in the areas of physical strength, coordination, soccer skills, sportsmanship, and team play.</li>
          <li>Maintain a positive environment.</li>
          <li>Be primarily responsible for coaching, and rely on the board and other individuals for administrative support.</li>
          <li>Continually look for opportunities to improve his/her knowledge about soccer, such as through advanced licensing.</li>
        </ul>

        <h4>Coaches Shall:</h4>
        <ul class="pad-b--20">
          <li>Be of high moral character.</li>
          <li>Be knowledgeable in basic soccer skills and the principles of team play.</li>
          <li>Provide opportunities for individual growth of all players in the areas of physical strength, coordination, soccer skills, sportsmanship, and team play.</li>
          <li>Maintain a positive environment.</li>
          <li>Be primarily responsible for coaching, and rely on the board and other individuals for administrative support.</li>
          <li>Continually look for opportunities to improve his/her knowledge about soccer, such as through advanced licensing.</li>
        </ul>

        <p><strong>Coach licensing requirements</strong> – each licensing paid for by the Academia De Futeboltraining, effective 1-1-15, will have the following commitment to the academy: E – 1 year, D – 2 years, C – 2 years, additional licensing commitment to be determined. Upon termination of coaching during the commitment period, the coach agrees to repay the academy the entire licensing fee paid by the academy within 2 months of terminating as a coach unless the coach retires from coaching for the remainder of the obligation.</p>

        <h4>Parents Shall:</h4>
        <ul class="pad-b--20">
          <li>Respect the dignity of all soccer participants and assist in the building of a positive environment.</li>
          <li>Provide administrative support when circumstances allow.</li>
          <li>Help meet financial obligations in a timely manner, and shall encourage their youth to participate in and support academy fundraisers in an effort to minimize the financial burden to individual families.</li>
        </ul>

        <h4>Executive Board Shall:</h4>
        <p>Be elected by current coaches and board members annually at the January academy meeting, and/or as a need arises and change is determined necessary. The Executive Board shall consist of no more than six members. The presence of at least half of the Executive Board shall constitute a quorum. The Executive Board shall never overrule a majority vote by the coaches on any issue.</p>
        <ul class="pad-b--20">
          <li><strong>The Executive Board members shall be:</strong></li>
          <li>President</li>
          <li>Vice President</li>
          <li>Secretary</li>
          <li>Treasurer</li>
          <li>Tournament Director</li>
          <li>Coaching Director</li>
        </ul>
        <ul class="pad-b--20">
          <li>Hold regular monthly open meetings during the season, or as needed.</li>
          <li>Coordinate with State and District leadership on all matters affecting the academy.</li>
          <li>Present potential coaching candidates and provide administrative support to the coaches.</li>
          <li>Provide opportunities for coaches and players to improve their skills through workshops and clinics.</li>
        </ul>
        <ul class="pad-b--20">
          <li>Encourage parental involvement in academy administrative functions.</li>
          <li>Discipline academy participants where needed.</li>
          <li>Vote on any academy expenditure that exceeds $50.00.</li>
        </ul>

        <h4>Coach Selection:</h4>
        <p>Posting for new coaches will be based on need as determined by the Executive Board. This need could be due to an existing coach leaving and/or new teams being established. The Executive Board determination on existing coaches leaving and new coaches being selected can be made at any time. Coaches for new teams will be determined in the spring of each year, prior to tryouts held in May/June and fall registration mid-June.</p>
        <p>Notification of an open coaching position will be announced by word of mouth and other advertisement by the Executive Board. All individuals interested in applying for a coaching position within the Striker organization are required to submit a formal resume with references. All resumes will be reviewed by the Executive Board and the coaches. A majority vote will be necessary for approval. If a Striker Executive Board/Coaches meeting is not able to be held, voting and selection of coaches will be done through alternate means, i.e., email transmittals. Selection of coaches for teams will be based on qualifications, which include licenses held, coaching experience, and references.</p>
        <p class="pad-b--20">Any coach will be required to be certified with an “E” license by the end of the first year of coaching a District team. A waiver must be submitted for the first year. Also, any coach will be required to be certified with a “D” licenses if coaching a State team. Waivers are only good for a one year time period, beginning the first year.</p>

        <h4>Team Selection:</h4>
        <p>Tryouts will be held each spring for all age groups on a date, time, and place established by each individual coach. Tryouts must be advertised and are mandatory if a new team is being established or the coach is desirous of replacing players.</p>
        <p>Each coach having his/her age group tryout will be responsible for organizing his/her tryouts. Each coach will express his interest to each player. The PLAYER AND PARENTS will make the decision where the player will play when offered positions for different teams. Beyond extending an initial invitation to join a team, there will be no soliciting of academy players by coaches within the academy.</p>
        <p class="pad-b--20">Tryouts will be open to any youth wanting to participate in their specific age group. Individuals requesting to play in a different age group would need to have approval from the DOC and Board Members.</p>

        <h4>Team Uniforms, Warm-Ups &amp; Gear:</h4>
        <p>The official academy uniform, warm-ups and gear, to be worn and used by all ADF affiliated teams, shall be determined and approved by the executive board and conform to the following guidelines:</p>
        <p>Jerseys will have the approved ADF logo placed on the Left chest and the US Youth Soccer logo placed on the Right sleeve. Individual player numbers and or names will be centered on the back of the jersey.</p>
        <ul class="pad-b--20">
          <li>Shorts will have individual, player numbers placed on the Front of the Left leg.</li>
          <li>Teams needing new uniforms are required to purchase the approved ADF uniform.</li>
          <li>All teams will be required to wear the approved ADF uniform each season.</li>
        </ul>

        <h4>Funds:</h4>
        <p class="pad-b--20">Money will be run through the Treasurer. Have all checks made out to Academia De Futeboltraining. The Treasurer will issue checks as requested by coaches or needed by the league. There will be no checks issued from a team account where funds do not exist to cover that check.</p>

        <h4>Additions or Deletions to Guidelines:</h4>
        <p class="pad-b--20">Any additions or deletions to these guidelines will be proposed by the Executive Board and will require a majority vote by the coaches for ratification. The guidelines must be approved by a majority vote of the coaches and Executive Board in January of each year.</p>

        <h4>Voting:</h4>
        <p class="pad-b--20">Each head coach of a current team will be entitled to one (1) vote. Each Executive Board member will be entitled to one (1) vote. An individual that coaches multiple teams will only be entitled to one (1) vote. Coaches that serve as Executive Board members shall be entitled to only one (1) vote. Voting may take place by alternate means (i.e. e-mail, telephone, proxy) provided that all eligible voting members are notified of the vote and a deadline is set for a response.</p>

        <h4>Parliamentary Authority:</h4>
        <p class="pad-b--20">The rules contained in the current edition of Robert’s Rules of Order Newly Revised shall govern the academy in all cases to which they are applicable and in which they are not inconsistent with these bylaws and any special rules of order the academy may adopt.</p>

        <h4>Academy Fees:</h4>
        <p>Academy dues will be determined by the board. The amount shall be kept as low as possible, in order to fund the academy activities and the development of the academy and its players but not prohibit participation because of financial hardship.</p>

